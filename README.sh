## Ansible for DevOps

Wanted to learn more about Ansible so I picked up the 20151016 version of Jeff Geerling's "Ansible for DevOps" book.

I'll keep relevant Vagrantfiles, hosts files, playbooks, and a dump of history commands organized by chapter.

I found that symlinking the /etc/ansible/hosts file to your current working directory's hosts file makes for easy review of prior chapters.
